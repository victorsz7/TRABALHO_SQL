CREATE DATABASE FACULDADE_DB;
USE FACULDADE_DB;

DROP TABLE TB_NOTAS;

CREATE TABLE TB_ALUNOS(
ID_ALUNO INT PRIMARY KEY AUTO_INCREMENT,
NM_ALUNO VARCHAR (200),
DS_EMAIL VARCHAR (200),
DS_CPF BIGINT,
DT_NASC DATE,
NM_CURSO VARCHAR (200)
);

CREATE TABLE TB_PROFESSORES(
ID_PROFESSOR INT PRIMARY KEY AUTO_INCREMENT,
NM_PROFESSOR VARCHAR (200),
DS_EMAIL VARCHAR (200),
DS_FORMACAO VARCHAR (200)
);

CREATE TABLE TB_TURMAS(
ID_TURMA INT PRIMARY KEY AUTO_INCREMENT,
NM_PROFESSOR VARCHAR (200),
NM_DISCIPLINA VARCHAR (200),
DS_SEMESTRE MIDDLEINT
);

CREATE TABLE TB_CURSOS(
ID_CURSO INT PRIMARY KEY AUTO_INCREMENT,
NM_CURSO VARCHAR (200),
DT_DURACAO DATE 
);

CREATE TABLE TB_DISCIPLINAS (
ID_DISCIPLINA INT PRIMARY KEY AUTO_INCREMENT,
NM_PROFESSOR VARCHAR (200),
NM_CURSO VARCHAR (200),
QTD_CARGAHORARIA BIGINT
);

CREATE TABLE TB_NOTAS(
ID_NOTA INT PRIMARY KEY AUTO_INCREMENT,
NM_ALUNO VARCHAR (200),
NM_TURMA VARCHAR (200),
DS_NOTA DECIMAL (10,1)
);

